const ALL_MODELS = [...IMAGE_MODELS, ...VIDEO_MODELS];

export function getImageModels(activeOnly = true): ModelEntry[] {
  return IMAGE_MODELS.filter(m => !activeOnly || m.is_active);
}

export function getVideoModels(activeOnly = true): ModelEntry[] {
  return VIDEO_MODELS.filter(m => !activeOnly || m.is_active);
}

export function getAllModels(activeOnly = true): ModelEntry[] {
  return ALL_MODELS.filter(m => !activeOnly || m.is_active);
}

export function getModelById(id: string): ModelEntry | undefined {
  return ALL_MODELS.find(m => m.id === id);
}

export function getDefaultImageModel(): ModelEntry {
  const envDefault = process.env.DEFAULT_IMAGE_MODEL;
  if (envDefault) {
    const model = ALL_MODELS.find(m => m.id === envDefault && m.is_active);
    if (model) return model;
  }
  return IMAGE_MODELS.find(m => m.is_recommended_default && m.is_active) ?? IMAGE_MODELS[0];
}

export function getDefaultVideoModel(): ModelEntry {
  const envDefault = process.env.DEFAULT_VIDEO_MODEL;
  if (envDefault) {
    const model = ALL_MODELS.find(m => m.id === envDefault && m.is_active);
    if (model) return model;
  }
  return VIDEO_MODELS.find(m => m.is_recommended_default && m.is_active) ?? VIDEO_MODELS[0];
}
