// GioHomeStudio — Model Registry
// Source of truth for all available image and video generation models.
// UI reads from this registry. Gateways route based on provider + endpoint.
// No other file should call providers directly.

export interface ModelEntry {
  id: string;
  display_name: string;
  provider_name: "segmind" | "fal";
  model_manufacturer: string;
  gateway: "segmind" | "fal";
  endpoint_id: string;           // provider-specific model/endpoint identifier
  type: "image" | "image_edit" | "video";
  quality_tier: string;
  cost_to_henry: number;         // cost per generation in USD
  price_to_user: number;         // what user pays (future billing)
  profit_margin: number;         // price_to_user - cost_to_henry
  unit: string;                  // "per_image" | "per_clip" | "per_second"
  resolution: string;
  max_duration_seconds: number | null;
  avg_generation_seconds: number;
  is_free_tier_eligible: boolean;
  is_recommended_default: boolean;
  is_auto_selected_if_cheaper: boolean;
  tags: string[];
  strengths: string;
  best_for: string;
  sort_price_rank: number;       // lower = cheaper
  sort_quality_rank: number;     // higher = better quality
  sort_trending_score: number;
  sort_usage_count: number;
  is_active: boolean;
  notes: string;
}

